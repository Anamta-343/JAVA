import java.util.Scanner;

public class Loops {
    public static void main(String args[]){
                                                //    for loop
        for(int i=0;i<=10;i++){                              
            System.out.print(i + " ");
        }
                                                //    While Loop
        int i=0;                                            
        while(i<=10){
            System.out.println(i);
            i+=1;
        }

        int a=12;
        while(a<=11){
            System.out.println(a);
            a+=1;
        }
                                        //       DO WHILE
        int b=0;                                            
        do{
            System.out.println(b);
            b+=1;
        }while(b<=10);

        int c=12;
        do{
            System.out.println(c);
            c+=1;
        }while(c<=11);
    }
}                                            
        